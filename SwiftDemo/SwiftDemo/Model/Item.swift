import UIKit

struct DataCollection {
    let id: Int
    let title:String
    var item: [Item]
    
    mutating func add(newItem: Item) {
        item.append(newItem)
    }
    mutating func remove(newItem: Item) {
        item = item.filter({$0.id != newItem.id})
    }
}
